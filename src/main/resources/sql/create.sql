CREATE TABLE playlist(
id VARCHAR(20) NOT NULL PRIMARY KEY,
title VARCHAR(100) NOT NULL,
label VARCHAR(100),
description TEXT,
share VARCHAR(10),
collect VARCHAR(10),
comment VARCHAR(10),
createdBy VARCHAR(30),
createdDate VARCHAR(10),
musicList TEXT
)ENGINE=INNODB DEFAULT CHARSET=UTF-8;

CREATE TABLE music(
id VARCHAR(20) NOT NULL PRIMARY KEY,
title VARCHAR(100) NOT NULL,
subTitle VARCHAR(100),
songer VARCHAR(30),
albumId VARCHAR(20),
lyrics TEXT,
comment VARCHAR(10)
)ENGINE=INNODB DEFAULT CHARSET=UTF-8;